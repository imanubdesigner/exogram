// ─── Store ─────────────────────────────────────────────────────────────────────

export const useI18nStore = defineStore('i18n', () => {
    const stored = localStorage.getItem('exogram_locale')
    const locale = ref(stored === 'en' ? 'en' : 'es')

    const dict = computed(() => translations[locale.value])

    /**
     * Traduce una clave con notación de punto.
     * Si la clave devuelve un objeto/array, lo retorna tal cual.
     * Uso: t('landing.hero.tagline')
     */
    function t(key, params = null) {
        const keys = key.split('.')
        let val = dict.value
        for (const k of keys) {
            if (val == null || typeof val !== 'object') return key
            val = val[k]
        }
        if (typeof val === 'string' && params && typeof params === 'object') {
            return Object.entries(params).reduce((result, [paramKey, paramValue]) => {
                const token = `{${paramKey}}`
                return result.replaceAll(token, String(paramValue))
            }, val)
        }
        return val ?? key
    }

    function toggleLocale() {
        locale.value = locale.value === 'es' ? 'en' : 'es'
        localStorage.setItem('exogram_locale', locale.value)
    }

    function setLocale(lang) {
        if (lang === 'es' || lang === 'en') {
            locale.value = lang
            localStorage.setItem('exogram_locale', lang)
        }
    }

    return { locale, t, toggleLocale, setLocale }
})
